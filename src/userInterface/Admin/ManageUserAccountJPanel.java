    private void popOrganizationComboBox() {
        organizationJComboBox.removeAllItems();
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationDirectory()) {
            organizationJComboBox.addItem(organization);
        }
    }
 
    private void populateAccountTable() {
       DefaultTableModel model = (DefaultTableModel) userJTable.getModel();
        model.setRowCount(0);
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationDirectory()) {
            for (Account ua : organization.getAccountDirectory().getAccountDirectory()) {
                Object row[] = new Object[1];
                row[0] = ua;
                
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
            }
        }
    }

    private void populateEmployeeComboBox(Organization organization) {
        employeeJComboBox.removeAllItems();

        for (Employee employee : organization.getEmployeeDirectory().getEmployeeDirectory()) {
            employeeJComboBox.addItem(employee);
        }
    }

    private void populateRoleComboBox(Organization organization) {
        roleJComboBox.removeAllItems();
        for (Role role : organization.getSupportedRoles()) {
            roleJComboBox.addItem(role);
        }
    }
