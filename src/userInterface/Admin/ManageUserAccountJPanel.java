    private void deleteBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBttnActionPerformed
        // TODO add your handling code here:
        int selectedRow = userJTable.getSelectedRow();
        int pic = JOptionPane.ERROR_MESSAGE;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select an Account to delete", "", pic);
        } else {
            Organization organization = null;
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the Account", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Account userAccount = (Account) userJTable.getValueAt(selectedRow, 0);
                for (Organization org : enterprise.getOrganizationDirectory().getOrganizationDirectory()) {
                    for (Account ua : org.getAccountDirectory().getAccountDirectory()) {
                        if (userAccount.getUsername().equals(ua.getUsername())) {
                            organization = org;
                            break;
                        }
                    }
                }
                organization.getAccountDirectory().removeAccount(userAccount);
                JOptionPane.showMessageDialog(null, "Account Deleted Successfully", "", pic);
                populateAccountTable();

            }
        }
    }//GEN-LAST:event_deleteBttnActionPerformed


