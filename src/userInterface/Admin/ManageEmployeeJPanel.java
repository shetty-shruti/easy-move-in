    private void deleteEmployeeBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteEmployeeBttnActionPerformed
        // TODO add your handling code here:
        int selectedRow = employeeJTable.getSelectedRow();
        int pic = JOptionPane.ERROR_MESSAGE;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select an Employee to delete", "", pic);
        } else {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the Employee", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Employee employee = (Employee) employeeJTable.getValueAt(selectedRow, 0);
                Organization organization = (Organization) organizationJComboBox.getSelectedItem();
                organization.getEmployeeDirectory().removeEmployee(employee);
                JOptionPane.showMessageDialog(null, "Employee Deleted Successfully", "", pic);
                populateEmployeeTable(organization);
            }
        }
        
    }//GEN-LAST:event_deleteEmployeeBttnActionPerformed

