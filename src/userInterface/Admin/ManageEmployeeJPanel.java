    public void populateEmployeeTable(Organization organization) {
        DefaultTableModel model = (DefaultTableModel) employeeJTable.getModel();
        model.setRowCount(0);
        for (Employee employee : organization.getEmployeeDirectory().getEmployeeDirectory()) {
            Object[] row = new Object[1];
            row[0] = employee;           
            model.addRow(row);
        }
    }
