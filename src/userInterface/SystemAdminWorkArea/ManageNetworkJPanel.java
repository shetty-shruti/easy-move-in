    private void manageNetworkBttn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manageNetworkBttn2ActionPerformed
        // TODO add your handling code here:
        String name = networkNameTxt.getText();
        int pic = JOptionPane.ERROR_MESSAGE;
        if (name.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Network", "", pic);
        } else {
            Network network = ecoSystem.addNetwork(name);
            JOptionPane.showMessageDialog(null, "Network Added Successfully");
            networkNameTxt.setText("");
            populateNetworkTable();
        }
    }//GEN-LAST:event_manageNetworkBttn2ActionPerformed

