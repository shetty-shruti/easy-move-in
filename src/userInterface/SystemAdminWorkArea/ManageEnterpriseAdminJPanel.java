    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseAdminJTable.getModel();

        model.setRowCount(0);
        for (Network network : ecoSystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseDirectory()) {
                for (Account userAccount : enterprise.getAccountDirectory().getAccountDirectory()) {
                    Object[] row = new Object[3];
                    row[0] = userAccount;
                    row[1] = enterprise;                   
                    row[2] = network;
                    model.addRow(row);
                }
            }
        }
    }
