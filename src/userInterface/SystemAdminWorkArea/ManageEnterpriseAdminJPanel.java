    private void deleteBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBttnActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        int selectedRow = enterpriseAdminJTable.getSelectedRow();
        int pic = JOptionPane.ERROR_MESSAGE;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select an Admin to delete", "", pic);
        } else {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete Admin", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Enterprise enterprise = (Enterprise) enterpriseAdminJTable.getValueAt(selectedRow, 1);
                Network network = (Network) enterpriseAdminJTable.getValueAt(selectedRow, 2);
                Account userAccount = (Account) enterpriseAdminJTable.getValueAt(selectedRow, 0);
                for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseDirectory()) {
                    if (e.getName().equalsIgnoreCase(enterprise.getName())) {
                        enterprise.getAccountDirectory().removeAccount(userAccount);
                        JOptionPane.showMessageDialog(null, "Account Deleted Successfully");
                        populateTable();
                    }
                }
            }
        }
    }//GEN-LAST:event_deleteBttnActionPerformed

