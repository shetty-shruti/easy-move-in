    private void populateNetworkAndEnterpriseComboBox() {
        networkJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();
        
        for (Network network : ecoSystem.getNetworkList()) {
            networkJComboBox.addItem(network);
        }
        
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            if(!(type == Enterprise.EnterpriseType.GOVERNMENT))
            enterpriseTypeJComboBox.addItem(type);
        }
    }

    public void populateEnterpriseTable() {
        DefaultTableModel dtm = (DefaultTableModel) enterpriseTable.getModel();
        dtm.setRowCount(0);
        for (Network network : ecoSystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseDirectory()) {
                Object row[] = new Object[3];
                row[0] = enterprise;
                row[1] = network;
                row[2] = enterprise.getEnterpriseType().getValue();
                dtm.addRow(row);
                
            }
        }
    }
