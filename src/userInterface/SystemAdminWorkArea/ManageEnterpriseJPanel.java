    private void addEnterpriseBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEnterpriseBttnActionPerformed
        // TODO add your handling code here:
        Network network = (Network) networkJComboBox.getSelectedItem();
        String name = enterpriseNameTxt.getText();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();
        if (network == null || type == null || name.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Enterprise Name!");
            return;
        }        
        Enterprise enterprise = network.getEnterpriseDirectory().addEnterprise(name, type);
        JOptionPane.showMessageDialog(null, "Enterprise Added Successfully");
        populateEnterpriseTable();
        enterpriseNameTxt.setText("");
    }//GEN-LAST:event_addEnterpriseBttnActionPerformed

