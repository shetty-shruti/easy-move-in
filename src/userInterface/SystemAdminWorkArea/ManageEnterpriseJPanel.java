    private void deleteBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBttnActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseTable.getSelectedRow();
        int pic = JOptionPane.ERROR_MESSAGE;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select an Enterprise to delete", "", pic);
        } else {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the Enterprise", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Enterprise enterpriseType = (Enterprise) enterpriseTable.getValueAt(selectedRow, 0);
                Network network = (Network) enterpriseTable.getValueAt(selectedRow, 1);
                network.getEnterpriseDirectory().removeEnterprise(enterpriseType);
                JOptionPane.showMessageDialog(null, "Deleted Successfully", "", pic);
                populateEnterpriseTable();
            }
        }
    }//GEN-LAST:event_deleteBttnActionPerformed

